{
}


void CMyApp::Render()
{
	// töröljük a frampuffert (GL_COLOR_BUFFER_BIT) és a mélységi Z puffert (GL_DEPTH_BUFFER_BIT)
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	// kapcsoljuk be a VAO-t (a VBO jön vele együtt)
	glBindVertexArray(m_vaoID);

	// kirajzolás
	glDrawArrays(GL_TRIANGLES, 0, 3 * tri_num); // háromszögek: 3, 6, 9, vagy 12 db vertex a 0.-tól kezdve
	glDrawArrays(GL_TRIANGLE_FAN, 12, N + 2); // kör: N+2 db vertex (középpont + N+1 a köríven) a 12.-tõl kezdve

	// VAO kikapcsolasa
	glBindVertexArray(0);
}

void CMyApp::KeyboardDown(SDL_KeyboardEvent& key)
{
	if (key.keysym.sym == SDLK_1){
		tri_num = 1;
	}
	if (key.keysym.sym == SDLK_2){
		tri_num = 2;
	}
	if (key.keysym.sym == SDLK_3){
		tri_num = 3;
	}
	if (key.keysym.sym == SDLK_4){
		tri_num = 4;
	}
}

